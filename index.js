// //  PREVENT FLAGS REPETITION
function unrepeatableFlags() {
    index = unrepeatableFlagsArray.indexOf(unrepeatableFlagsArray[randomFlagNumberC]);

    // prevent crash
    if (index == -1) {
        unrepeatableFlagsArray = JSON.parse(JSON.stringify(unionFlagsArray));
        index = unrepeatableFlagsArray.indexOf(unrepeatableFlagsArray[randomFlagNumberC]);
    };
    selectedFlagWithdrawn = unrepeatableFlagsArray.splice(index, 1);
};


// SHUFFLE ANSWERS
function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    };
};


